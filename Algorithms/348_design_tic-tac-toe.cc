/*
Keep track of the mark counts for both players on each row, col, diagonal and anti-diagonal.
When a player makes a move, increment the counter and also check if the value is equal to n. If yes the player has won.
Can further optimize to store a number which equals ‘X’ counts - ‘O’ counts (one player is 1 the other is -1). When the count is n or -n the player has won.
*/
class TicTacToe {
public:
    TicTacToe(int n) {
        this->n = n;
        row_counts.resize(n, vector<int>(2, 0));
        col_counts.resize(n, vector<int>(2, 0));
        diag_counts.resize(2, vector<int>(2, 0));
    }
    
    int move(int row, int col, int player) {
        if (++row_counts[row][player-1] == n) return player;
        if (++col_counts[col][player-1] == n) return player;
        if (row == col) {
            if (++diag_counts[0][player-1] == n) return player;
        }
        if (row + col == n - 1) {
            if (++diag_counts[1][player-1] == n) return player;
        }
        return 0;
    }
private:
    vector<vector<int>> row_counts, col_counts, diag_counts;
    int n;
};

